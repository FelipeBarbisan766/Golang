package main
	import "fmt"
	func main() {
		fmt.Println("Hello, World!")

		//! Declaração de Variaveis
		//? => Normal: usando plavra reservada "var"
		var nome_01 string = "Ronaldo"
		//? => Short Variable Declaration: usando ":="
		nome_02 := "Felipe"

		fmt.Println("Nome 01: ", nome_01)
		fmt.Println("Nome 02: ", nome_02)
		
	}